    public static string Get(string culture, string key)
    {
        var normalizedCulture = AppCultures.Normalize(culture);

        if (Resources.TryGetValue(normalizedCulture, out var cultureResources) &&
            cultureResources.TryGetValue(key, out var localizedValue))
        {
            return localizedValue;
        }

        if (Resources[AppCultures.DefaultCulture].TryGetValue(key, out var fallbackValue))
            return fallbackValue;

        return key;
    }
